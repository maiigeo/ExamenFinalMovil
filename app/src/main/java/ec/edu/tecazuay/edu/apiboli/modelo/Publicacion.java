package ec.edu.tecazuay.edu.apiboli.modelo;

public class Publicacion {

  private int userId;
  private int id;
  private String titulo;

  public Publicacion() {
  }

  public int getUserId() {
    return userId;
  }

  public void setUserId(int userId) {
    this.userId = userId;
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getTitulo() {
    return titulo;
  }

  public void setTitulo(String titulo) {
    this.titulo = titulo;
  }



}
